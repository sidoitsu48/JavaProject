    private void jMakeAdresseMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMakeAdresseMouseClicked

        Stammdaten name = new Stammdaten();
        String vorname = name.getVorname();
        String initial = name.getInitial();
        String nachname = name.getNachname();
        
        jNameField.setText(makeName(vorname, initial, nachname));
        jStrasseField.setText(name.getStrasse());
        jPLZField.setText(name.getPlz());
        jStadtField.setText(name.getStadt());
        jTelefonField.setText(name.getTelefon());
        jEmailField.setText(name.getEmail(vorname, nachname));
    }//GEN-LAST:event_jMakeAdresseMouseClicked

